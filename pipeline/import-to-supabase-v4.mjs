// ReviewIntel v4 — Supabase Importer
// Reads analyzed-v4-all-dated.json and upserts into competitor_reviews table.
//
// Usage:
//   SUPABASE_SERVICE_KEY=xxx node import-to-supabase-v4.mjs
//   SUPABASE_SERVICE_KEY=xxx node import-to-supabase-v4.mjs --dry-run
//   SUPABASE_SERVICE_KEY=xxx node import-to-supabase-v4.mjs --provider inkout-houston-tx
//   SUPABASE_SERVICE_KEY=xxx node import-to-supabase-v4.mjs --status published
//
// Strategy:
//   DELETE rows matching provider_name + location_city + location_state + scrape_version
//   then INSERT fresh rows. This means a re-import replaces old data for that
//   provider/location without touching other providers.

import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.join(__dirname, '..');

// ── Bucket lookup (from separator output) ───────────────────────────────────
// Key: "reviewer_name|review_date_iso|location_city" → {bucket, routing_reason}
// Generated by: python3 separator/run.py — see separator/run.py write_bucket_lookup
function loadBucketLookup() {
  const bucketPath = path.join(ROOT, 'output/bucket_lookup.json');
  if (!fs.existsSync(bucketPath)) {
    console.warn('WARNING: output/bucket_lookup.json not found — bucket field will be null for all rows');
    console.warn('  Run the separator first: python3 separator/run.py');
    return new Map();
  }
  const obj = JSON.parse(fs.readFileSync(bucketPath, 'utf8'));
  // Support both old format (string value) and new format ({bucket, routing_reason})
  const lookup = new Map(
    Object.entries(obj).map(([k, v]) => [
      k,
      typeof v === 'string' ? { bucket: v, routing_reason: null, last_analyzed_at: null } : v,
    ])
  );
  console.log(`Bucket lookup loaded: ${lookup.size} inkOUT reviews mapped`);
  return lookup;
}

const BUCKET_LOOKUP = loadBucketLookup();

const SUPABASE_URL = 'https://rxrhvbfutjahgwaambqd.supabase.co';
const SERVICE_KEY  = process.env.SUPABASE_SERVICE_KEY;
const TABLE        = 'competitor_reviews';

if (!SERVICE_KEY) {
  console.error('Missing SUPABASE_SERVICE_KEY');
  console.error('Find it in: Supabase Dashboard > Project Settings > API > service_role');
  process.exit(1);
}

const args        = process.argv.slice(2);
const DRY_RUN     = args.includes('--dry-run');
const STATUS      = args.find(a => a.startsWith('--status='))?.split('=')[1] ?? 'draft';
const ONLY_SLUG   = args.find(a => a.startsWith('--provider='))?.split('=')[1] ?? null;
const BATCH_SIZE  = 200;

const HEADERS = {
  'apikey':        SERVICE_KEY,
  'Authorization': `Bearer ${SERVICE_KEY}`,
  'Content-Type':  'application/json',
  'Prefer':        'return=minimal',
};

async function supabase(method, path, body = null) {
  const res = await fetch(`${SUPABASE_URL}/rest/v1/${path}`, {
    method,
    headers: HEADERS,
    body: body ? JSON.stringify(body) : null,
  });
  if (!res.ok) {
    const text = await res.text();
    throw new Error(`${method} ${path} → ${res.status}: ${text}`);
  }
}

function toRow(review, status) {
  return {
    provider_name:          review.provider_name ?? null,
    brand_name:             review.brand_name ?? null,
    location_city:          review.location_city ?? null,
    location_state:         review.location_state ?? null,
    method_used:            review.method_used ?? null,

    review_text:            review.review_text || null,
    star_rating:            typeof review.star_rating === 'number' ? review.star_rating : null,
    reviewer_name:          review.reviewer_name || null,
    reviewer_local_guide:   review.reviewer_local_guide ?? false,
    verified_source:        review.verified_source || 'Google',

    review_date:            review.review_date || null,
    review_date_iso:        review.review_date_iso || null,
    review_date_edited_iso: review.review_date_edited_iso || null,
    review_date_estimated:  review.review_date_estimated || null,
    review_date_label:      review.review_date_label || null,
    review_date_source:     review.review_date_source || 'estimated',

    result_rating:          review.result_rating ?? null,
    pain_level:             review.pain_level != null ? String(review.pain_level) : null,
    scarring_mentioned:     review.scarring_mentioned ?? null,
    sessions_completed:     review.sessions_completed != null ? String(review.sessions_completed) : null,
    skin_type:              review.skin_type ?? null,
    use_case:               review.use_case ?? null,

    has_text:               review.has_text ?? true,
    location_transition:    review.location_transition ?? false,
    transition_note:        review.transition_note ?? null,
    multi_location_brand:   review.multi_location_brand ?? false,

    _place_title:           review._place_title ?? null,
    _place_id:              review._place_id ?? null,
    _scrape_date:           review._scrape_date ?? null,
    _scrape_mode:           review._scrape_mode ?? 'full',
    scrape_version:         'v4',

    source_url:             review.source_url || null,

    is_tattoo_removal: null,  // populated by classify-relevance.mjs after import

    bucket: (() => {
      const key = `${review.reviewer_name}|${review.review_date_iso}|${review.location_city}`;
      return BUCKET_LOOKUP.get(key)?.bucket ?? 'competitor';
    })(),

    routing_reason: (() => {
      const key = `${review.reviewer_name}|${review.review_date_iso}|${review.location_city}`;
      return BUCKET_LOOKUP.get(key)?.routing_reason ?? null;
    })(),

    last_analyzed_at: (() => {
      const key = `${review.reviewer_name}|${review.review_date_iso}|${review.location_city}`;
      return BUCKET_LOOKUP.get(key)?.last_analyzed_at ?? null;
    })(),

    // review_required reviews are always pending until a company user approves them
    status: (() => {
      const key = `${review.reviewer_name}|${review.review_date_iso}|${review.location_city}`;
      return BUCKET_LOOKUP.get(key)?.bucket === 'review_required' ? 'pending' : status;
    })(),
  };
}

async function run() {
  console.log(`\nReviewIntel v4 — Supabase Importer`);
  console.log(`Table:   ${TABLE}`);
  console.log(`Status:  ${STATUS}`);
  if (DRY_RUN)   console.log(`Mode:    DRY RUN — no writes`);
  if (ONLY_SLUG) console.log(`Filter:  ${ONLY_SLUG} only`);
  console.log('');

  const analyzedPath = path.join(ROOT, 'data/analyzed/analyzed-v4-all-dated.json');
  const reviews = JSON.parse(fs.readFileSync(analyzedPath, 'utf8'));
  console.log(`Loaded ${reviews.length} reviews from data/analyzed/analyzed-v4-all-dated.json\n`);

  // Group by provider + city + state so we can delete and re-insert per location
  const groups = {};
  for (const r of reviews) {
    const key = `${r.provider_name}|${r.location_city}|${r.location_state}`;
    if (!groups[key]) groups[key] = [];
    groups[key].push(r);
  }

  let totalInserted = 0;
  let totalDeleted  = 0;
  const errors      = [];

  for (const [key, groupReviews] of Object.entries(groups)) {
    const [providerName, city, state] = key.split('|');

    // If --provider filter is set, skip non-matching groups
    if (ONLY_SLUG) {
      const groupSlug = `${providerName}-${city}-${state}`.toLowerCase().replace(/[^a-z0-9]+/g, '-');
      if (!groupSlug.includes(ONLY_SLUG)) continue;
    }

    const rows = groupReviews.map(r => toRow(r, STATUS));
    console.log(`  ${providerName} (${city}, ${state}) — ${rows.length} rows`);

    if (DRY_RUN) {
      console.log(`    [dry-run] would delete + insert ${rows.length} rows\n`);
      continue;
    }

    try {
      // Delete existing rows for this provider+city+state+version
      const deleteFilter = [
        `provider_name=eq.${encodeURIComponent(providerName)}`,
        `location_city=eq.${encodeURIComponent(city)}`,
        `location_state=eq.${encodeURIComponent(state)}`,
        `scrape_version=eq.v4`,
      ].join('&');
      await supabase('DELETE', `${TABLE}?${deleteFilter}`);
      totalDeleted++;
      console.log(`    Deleted existing rows`);

      // Insert in batches
      for (let i = 0; i < rows.length; i += BATCH_SIZE) {
        await supabase('POST', TABLE, rows.slice(i, i + BATCH_SIZE));
      }
      totalInserted += rows.length;
      console.log(`    Inserted ${rows.length} rows with status="${STATUS}"\n`);
    } catch (err) {
      console.error(`    ERROR: ${err.message}\n`);
      errors.push(`${providerName} (${city}, ${state}): ${err.message}`);
    }

    await new Promise(r => setTimeout(r, 200));
  }

  console.log('══════════════════════════════════════════');
  console.log('Import complete');
  if (!DRY_RUN) {
    console.log(`  Locations processed: ${totalDeleted}`);
    console.log(`  Reviews inserted:    ${totalInserted}`);
    console.log(`  Status:              ${STATUS}`);
  }
  if (errors.length > 0) {
    console.log(`\n  Errors (${errors.length}):`);
    errors.forEach(e => console.log(`    - ${e}`));
  }
  console.log('══════════════════════════════════════════\n');

  if (!DRY_RUN && STATUS === 'draft') {
    console.log(`Next step: review data in the dashboard, then promote to published:`);
    console.log(`  SUPABASE_SERVICE_KEY=xxx node import-to-supabase-v4.mjs --status published\n`);
  }
}

run().catch(err => {
  console.error('Fatal:', err.message);
  process.exit(1);
});
